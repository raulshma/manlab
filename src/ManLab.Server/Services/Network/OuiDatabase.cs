using System.Collections.Frozen;

namespace ManLab.Server.Services.Network;

/// <summary>
/// OUI (Organizationally Unique Identifier) database for MAC address vendor lookups.
/// Contains a subset of common vendor prefixes. The full IEEE database has 50k+ entries.
/// </summary>
public sealed class OuiDatabase : IOuiDatabase
{
    private readonly FrozenDictionary<string, string> _vendors;

    public OuiDatabase()
    {
        _vendors = LoadOuiData().ToFrozenDictionary();
    }

    /// <inheritdoc />
    public int VendorCount => _vendors.Count;

    /// <inheritdoc />
    public string? LookupVendor(string macAddress)
    {
        if (string.IsNullOrWhiteSpace(macAddress))
        {
            return null;
        }

        // Extract first 3 octets (OUI prefix) - 6 characters
        var oui = macAddress
            .Replace(":", "")
            .Replace("-", "")
            .Replace(".", "")
            .ToUpperInvariant();

        if (oui.Length < 6)
        {
            return null;
        }

        oui = oui[..6];
        return _vendors.GetValueOrDefault(oui);
    }

